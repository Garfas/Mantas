#list_module
# |get_length(lst) paima sąrašą kaip įvestį ir grąžina 
# |jo ilgį naudodama funkciją len().
def get_length(lst): 
    return len(lst)
# |sort_list(lst) priima sąrašą kaip įvestį ir grąžina naują sąrašą, kuriame yra tie patys elementai kaip ir 
# |įvesties sąraše, bet surūšiuota didėjimo tvarka naudojant sorted() funkciją.
def sort_list(lst): #surūšiuoti sarašą
    return sorted(lst)
# |get_sum(lst) kaip įvestį paima skaičių sąrašą ir grąžina tų
# | skaičių sumą naudodamas sum() funkciją.
def get_sum(lst): #gauti suma
    return sum(lst)
